// Increment and decrement
#include <iostream>
using namespace std;
int main(){
    int x = 7;
        cout<<x<<endl;
    x++;
        cout<<x<<endl;
    x--;
        cout<<x<<endl;
    ++x;
        cout<<x<<endl;
    --x;
        cout<<x<<endl;
    cout<<x++<<endl;
    cout<<x--<<endl;
    cout<<++x<<endl;
    cout<<--x<<endl;
}